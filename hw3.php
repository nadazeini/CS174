/**
 * returns product of a 5 digit number
 */
function calcProduct ($fiveNum){
    $product = 1 ;
    
    for($i = 0; $i < strlen($fiveNum); $i ++){
        $product *= $fiveNum[$i];
    }
    return $product;
    
}
/* returns factorial of a number
 */
function factorial($numb){
  
    $factorial = 1;

    for($j = 1 ; $j <=  $numb; $j++){
        $factorial  = $factorial * $j;
        
    }
    return $factorial;
    
}
